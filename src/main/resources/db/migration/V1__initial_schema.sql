create table addresses (
    block_street integer,
    city_id bigint,
    id bigint not null,
    supplier_id bigint,
    user_id bigint,
    apartment varchar(90),
    flat varchar(90),
    street_name varchar(90) not null,
    primary key (id)
);

create table affiliates (
    birth_date date not null,
    deceased boolean default false,
    dni integer not null,
    start_date date,
    gender_id bigint,
    id bigint generated by default as identity,
    relationship_id bigint,
    user_id bigint,
    last_name varchar(70) not null,
    first_name varchar(100) not null,
    primary key (id),
    constraint index_dni unique (dni)
);

create table brands (
    id bigint not null,
    name varchar(95) not null,
    web_page varchar(95),
    primary key (id)
);

create table categories (
    id bigint not null,
    name varchar(75) not null,
    description varchar(255),
    primary key (id)
);

create table cities (
    id bigint not null,
    province_id bigint,
    zip_code varchar(10) not null,
    name varchar(100) not null,
    primary key (id)
);

create table confirmation_tokens (
    expiry_date timestamp(6) with time zone,
    id bigint not null,
    user_id bigint not null,
    token varchar(100) not null,
    primary key (id)
);

create table death_causes (
    id bigint not null,
    name varchar(150) not null,
    primary key (id)
);

create table deceased (
    affiliated boolean default false,
    birth_date date not null,
    death_date date not null,
    dni integer not null,
    address_id bigint unique,
    death_cause_id bigint,
    gender_id bigint,
    id bigint not null,
    register_date timestamp(6),
    relationship_id bigint,
    user_id bigint,
    last_name varchar(100) not null,
    first_name varchar(150) not null,
    primary key (id)
);

create table funeral (
    tax numeric(5,2),
    total_amount numeric(11,2) not null,
    deceased_id bigint unique,
    funeral_date timestamp(6) not null,
    id bigint generated by default as identity,
    plan_id bigint,
    receipt_type_id bigint,
    register_date timestamp(6),
    receipt_number varchar(50) not null unique,
    receipt_series varchar(50) not null,
    primary key (id)
);

create table genders (
    id bigint not null,
    name varchar(75) not null,
    primary key (id)
);

create table income_details (
    purchase_price numeric(8,2) not null,
    quantity integer not null,
    sale_price numeric(8,2) not null,
    id bigint not null,
    income_id bigint,
    item_id bigint,
    primary key (id)
);

create table incomes (
    deleted boolean not null,
    tax numeric(5,2),
    total_amount numeric(8,2) default 0,
    id bigint not null,
    income_date timestamp(6),
    last_modified_date timestamp(6),
    receipt_number bigint not null,
    receipt_series bigint not null,
    receipt_type_id bigint,
    supplier_id bigint,
    user_id bigint,
    user_modified_id bigint,
    primary key (id)
);

create table items (
    item_height numeric(8,2),
    item_length numeric(10,2),
    item_width numeric(8,2),
    price numeric(10,2),
    stock integer,
    brand_id bigint,
    category_id bigint,
    id bigint generated by default as identity,
    name varchar(85) not null,
    code varchar(95) unique,
    description varchar(255),
    item_image_link varchar(255),
    primary key (id)
);

create table items_plan (
    quantity integer not null,
    item_id bigint not null,
    plan_id bigint not null,
    primary key (item_id, plan_id)
);

create table mobile_numbers (
    id bigint not null,
    supplier_id bigint,
    user_id bigint,
    mobile_number varchar(255) not null,
    primary key (id)
);

create table plans (
    price numeric(11,2),
    profit_percentage numeric(9,2) not null,
    id bigint generated by default as identity,
    name varchar(100) not null,
    description text,
    image_url varchar(255),
    primary key (id)
);

