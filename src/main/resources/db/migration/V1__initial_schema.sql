    primary key (id)
);

create table refresh_tokens (
    expiry_date timestamp(6) with time zone not null,
    id bigint generated by default as identity,
    issued_at timestamp(6) with time zone not null,
    last_used_at timestamp(6) with time zone,
    refresh_count bigint,
    revoked_at timestamp(6) with time zone,
    user_device_id bigint not null unique,
    version bigint,
    token_hash varchar(64) not null unique,
    primary key (id)
);

create table relationships (
    id bigint not null,
    name varchar(60) not null,
    primary key (id)
);

create table roles (
    id bigint not null,
    name varchar(255) check ((name in ('ROLE_ADMIN', 'ROLE_USER'))),
    primary key (id)
);

create table suppliers (
    id bigint generated by default as identity,
    nif varchar(80) not null unique,
    email varchar(90) not null,
    web_page varchar(90),
    name varchar(100) not null,
    primary key (id)
);

create table user_devices (
    is_refresh_active boolean,
    id bigint generated by default as identity,
    last_seen_at timestamp(6) with time zone,
    token_version bigint,
    user_id bigint not null unique,
    version bigint,
    fingerprint_hash varchar(64) not null,
    device_id varchar(255) not null,
    device_type varchar(255),
    last_ip_address varchar(255),
    primary key (id)
);

create table user_role (
    role_id bigint not null,
    user_id bigint not null,
    primary key (role_id, user_id)
);

create table users (
    active boolean not null,
    enabled boolean,
    start_date date,
    id bigint not null,
    first_name varchar(90) not null,
    last_name varchar(90) not null,
    email varchar(150) not null,
    encrypted_password varchar(255) not null,
    primary key (id),
    constraint index_email unique (email)
);

